package ve.org.others.services.rest;


public class SolicitudCorreo {
	private String txDireccionCorreo;
	private String txMensajeCuerpoCorreo;
	private String txAsuntoCorreo;
	
	public String getTxDireccionCorreo() {
		return txDireccionCorreo;
	}
	public void setTxDireccionCorreo(String txDireccionCorreo) {
		this.txDireccionCorreo = txDireccionCorreo;
	}
	public String getTxMensajeCuerpoCorreo() {
		return txMensajeCuerpoCorreo;
	}
	public void setTxMensajeCuerpoCorreo(String txMensajeCuerpoCorreo) {
		this.txMensajeCuerpoCorreo = txMensajeCuerpoCorreo;
	}
	public String getTxAsuntoCorreo() {
		return txAsuntoCorreo;
	}
	public void setTxAsuntoCorreo(String txAsuntoCorreo) {
		this.txAsuntoCorreo = txAsuntoCorreo;
	}

}
